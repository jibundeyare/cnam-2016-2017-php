<?php

// affectation d'une valeur
$i = 0;

// affectation d'une valeur
$j = $i + 1;

echo $j;
echo "<br />\n";

// réaffectation (incrémentation) d'une valeur
$i = $i + 1;

echo $i;
echo "<br />\n";

// réaffectation (incrémentation) d'une valeur
// identique à `$i = $i + 1;`
$i++;

echo $i;
echo "<br />\n";

// réaffectation (décrémentation) d'une valeur
$i = $i - 1;

echo $i;
echo "<br />\n";

// réaffectation (décrémentation) d'une valeur
// identique à `$i = $i - 1;`
$i--;

echo $i;
echo "<br />\n";

